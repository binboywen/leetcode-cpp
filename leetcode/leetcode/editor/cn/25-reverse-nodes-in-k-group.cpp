/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    ListNode* reverseKGroup(ListNode* head, int k) {
        ListNode *temp = head;
        int length = 0;
        while (temp != nullptr) {
            length++;
            temp = temp->next;
        }

        int times = length / k;
        int i = 1;
        ListNode *newHead = new ListNode(-1);
        temp = newHead;
        ListNode *node = head;
        ListNode * saveNode;
        while (i <= times) {
            saveNode = node;
            i++;
            for (int j = 0; j < k; j++) {
                ListNode *tempNext = temp->next;
                ListNode *nodeNext = node->next;
                temp->next = node;
                node->next = tempNext;
                node = nodeNext;
            }
            temp = saveNode;
        }
        temp->next = node;
        return newHead->next;
    }
};
