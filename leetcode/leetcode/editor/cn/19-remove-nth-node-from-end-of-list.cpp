/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    ListNode* removeNthFromEnd(ListNode* head, int n) {
        ListNode *temp = head;
        int length = 0;
        while (temp != nullptr) {
            length++;
            temp = temp->next;
        }
        int removeAdd = length - n;
        if (removeAdd == 0) {
            return head->next;
        }
        ListNode *p = head;

        int j = 1;
        while (p->next != nullptr && (j < removeAdd)) {
            p = p->next;
            j++;
        }
        p->next = p->next->next;
        return head;
    }
};
