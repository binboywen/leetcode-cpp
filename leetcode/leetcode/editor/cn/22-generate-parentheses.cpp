class Solution {
    vector<string> res;
public:
    vector<string> generateParenthesis(int n) {
        backTrack(n, "", 0, 0);
        return res;
    }

    void backTrack(int n, string s,int left, int right) {
        if (s.size() == n * 2) {
            res.push_back(s);
            return;
        }
        if (left < n) {
            backTrack(n, s + "(", left + 1, right);
        }
        if (right < left) {
            backTrack(n, s + ")", left, right + 1);
        }

    }
};
