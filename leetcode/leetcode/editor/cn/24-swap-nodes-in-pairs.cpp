/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    ListNode* swapPairs(ListNode* head) {
        if (head == nullptr) {
            return nullptr;
        }
        ListNode *temp = new ListNode(-1);
        temp->next = head;
        backTrack(temp, temp->next, temp->next->next);
        return temp->next;

    }

    void backTrack(ListNode *head, ListNode *node1, ListNode *node2) {
        if (node1 == nullptr || node2 == nullptr) {
            return;
        }
        head->next = node2;
        node1->next = node2->next;
        node2->next = node1;
        if (node1->next == nullptr || node1->next->next == nullptr) {
            return;
        }
        backTrack(node1, node1->next, node1->next->next);
    }
};
