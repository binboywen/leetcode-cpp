class Solution {
public:
    int twoEggDrop(int n) {
        int dp[2 + 1][n + 1];
        memset(dp, 0, sizeof dp);
        for (int j = 1; j <= n; j++) {
            for (int i = 1; i <= 2; i++) {
                dp[i][j] = dp[i][j - 1] + dp[i - 1][j - 1] + 1;
                if (dp[i][j] >= n) {
                    return j;
                }
            }
        }
        return n;
    }
};
